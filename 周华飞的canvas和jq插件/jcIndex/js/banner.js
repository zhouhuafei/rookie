(function(doc){
        var oParent=doc.getElementById('banner');
        var oCut=oParent.getElementsByTagName('ul')[0];
        var aCut=oCut.getElementsByTagName('li');
        var oBtn=oParent.getElementsByTagName('ol')[0];
        var aBtn=oBtn.getElementsByTagName('li');
        var iLen=aCut.length;
        var iW=aBtn[0].offsetWidth;
        var allW=oBtn.parentNode.offsetWidth;
        var clientNum=Math.ceil(allW/iW);
        var now=0;
        var timer=null;
        var autoInterval=5000;
        var oPrev=oParent.getElementsByTagName('span')[0];
        var oNext=oParent.getElementsByTagName('span')[1];
        for(var i=0;i<iLen;i++){
                aBtn[i].index=i;
                aBtn[i].onclick=function(){
                        now=this.index;
                        tab();
                }
        }
        function tab(){
                for(var i=0;i<iLen;i++){
                        aBtn[i].className='';
                        aCut[i].className='';
                }
                aBtn[now].className='active';
                aCut[now].className='active';
                if(iLen>clientNum){
                        if(now>=iLen-2){
                                oBtn.style.left=-iW*(iLen-clientNum)+'px';
                        }else if(now>=clientNum-1){
                                oBtn.style.left=-iW*(now-(clientNum-1)+1)+'px';
                        }else{
                                oBtn.style.left=0;
                        }
                }
        }
        oPrev.onclick=function(){
                now--;
                if(now<0){
                        now=iLen-1;
                }
                tab();
        };
        function next(){
                now++;
                now%=iLen;
                tab();
        }
        oNext.onclick=next;
        function preventSelect(){
                return false;
        }
        oNext.onselectstart=oPrev.onselectstart=preventSelect;
        function autoPlay(){
                timer=setInterval(next,autoInterval);
        }
        autoPlay();
        function stopAutoPlay(){
                clearInterval(timer);
        }
        oParent.onmouseover=stopAutoPlay;
        oParent.onmouseout=autoPlay;
})(document);